const verificationLabels = {
  "source-confirmed": "已核實來源",
  developing: "追蹤中",
  corrected: "已修正"
};

const requiredFields = [
  ["id"],
  ["title"],
  ["summary"],
  ["url"],
  ["source", "name"],
  ["section"],
  ["dates", "published"]
];

const getField = (story, path) => path.reduce((value, key) => value?.[key], story);

const validateStories = (candidateStories) => {
  const seenIds = new Set();
  const seenUrls = new Set();
  const errors = [];
  const valid = candidateStories.filter((story, index) => {
    const missing = requiredFields.filter((path) => !getField(story, path)).map((path) => path.join("."));
    const duplicateId = story.id && seenIds.has(story.id);
    const duplicateUrl = story.url && seenUrls.has(story.url);

    if (story.id) {
      seenIds.add(story.id);
    }

    if (story.url) {
      seenUrls.add(story.url);
    }

    if (!missing.length && !duplicateId && !duplicateUrl) {
      return true;
    }

    errors.push({
      index,
      id: story.id || "(missing id)",
      missing,
      duplicateId,
      duplicateUrl
    });
    return false;
  });

  return { valid, errors };
};

const { valid: validStories, errors: validationErrors } = validateStories(stories);

if (validStories.length !== stories.length) {
  console.error("Story validation failed; rendering valid stories only.", {
    total: stories.length,
    valid: validStories.length,
    errors: validationErrors
  });
}

const featuredCount = validStories.filter((story) => story.featured).length;

if (featuredCount !== 1) {
  console.error(`Expected exactly one featured story, found ${featuredCount}.`);
}

